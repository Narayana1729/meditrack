-- Enable basic RLS (Row Level Security) so anonymous API requests can read/insert
ALTER TABLE public.doctors ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.health_records ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.profiles ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Enable read access for all users" ON public.doctors FOR SELECT USING (true);
CREATE POLICY "Enable update access for all users" ON public.doctors FOR UPDATE USING (true);

CREATE POLICY "Enable read access for all users" ON public.health_records FOR SELECT USING (true);
CREATE POLICY "Enable insert access for all users" ON public.health_records FOR INSERT WITH CHECK (true);
CREATE POLICY "Enable delete access for all users" ON public.health_records FOR DELETE USING (true);

CREATE POLICY "Enable read access for all users" ON public.profiles FOR SELECT USING (true);
