-- 2. Health Records Table
CREATE TABLE IF NOT EXISTS public.health_records (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  type TEXT NOT NULL,
  date DATE NOT NULL,
  doctor TEXT,
  file TEXT,
  file_type TEXT,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
