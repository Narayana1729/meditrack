-- 3. Profiles Table
CREATE TABLE IF NOT EXISTS public.profiles (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name TEXT NOT NULL,
  age INTEGER,
  blood TEXT,
  email TEXT,
  phone TEXT,
  conditions JSONB,
  allergies JSONB,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

